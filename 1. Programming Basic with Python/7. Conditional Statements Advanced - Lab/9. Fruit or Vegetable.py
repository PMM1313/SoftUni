
name_of_fruit = str(input())

if name_of_fruit == 'banana' \
        or name_of_fruit == 'apple' \
        or name_of_fruit == 'kiwi' \
        or name_of_fruit == 'cherry' \
        or name_of_fruit == 'lemon' \
        or name_of_fruit == 'grapes':
    print('fruit')

elif name_of_fruit == 'tomato' \
        or name_of_fruit == 'cucumber' \
        or name_of_fruit == 'pepper' \
        or name_of_fruit == 'carrot':
    print('vegetable')

else:
    print('unknown')
